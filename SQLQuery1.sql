SELECT 
    DATEDIFF(YEAR, '2006-03-26', GETDATE()) AS Years,
    DATEDIFF(MONTH, '2006-03-26', GETDATE()) AS Months,
    DATEDIFF(WEEK, '2006-03-26', GETDATE()) AS Weeks,
    DATEDIFF(HOUR, '2006-03-26', GETDATE()) AS Hours,
    DATEDIFF(MINUTE, '2006-03-26', GETDATE()) AS Minutes,
    DATEDIFF(SECOND, '2006-03-26', GETDATE()) AS Seconds